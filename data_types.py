# Integer data type (whole numbers)
number = 5
print("Integer:", number)

# Float data type (decimal numbers)
float_number = 3.14
print("Float:", float_number)

# String data type (text)
text = "Hello, World!"
print("String:", text)

# Boolean data type (True or False)
is_true = True
is_false = False
print("Boolean (True):", is_true)
print("Boolean (False):", is_false)

# List data type (ordered collection of items)
my_list = [1, 2, 3, "four", "five"]
print("List:", my_list)

# Tuple data type (ordered, immutable collection of items)
my_tuple = (1, 2, 3, "four", "five")
print("Tuple:", my_tuple)

# Dictionary data type (key-value pairs)
my_dict = {
    "name": "Happiness", 
    "age": 10, 
    "city": "Zomba"
    }
print("Dictionary:", my_dict)

# Set data type (unordered collection of unique items)
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)